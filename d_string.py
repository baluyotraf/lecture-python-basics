# String Code Sample

# String Definition
string1 = 'Hello World!'
string2 = "Hello World!"
string3 = 'Hello ' "World!"

print(string1, string2, string3)

# String Concatenation
print(string1 + string2)

# String Multiplication
print(string1 * 3)

# String transformation
print(string1.upper())
print(string1.lower())
print(string1.split(' '))

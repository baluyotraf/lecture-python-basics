# Membership Operator Sample Code

string_1 = 'Hello World!'
tuple_1 = tuple('abc')

# Membership
print('Hello' in string_1)
print('Hi' in string_1)
print('a' in tuple_1)
print('d' in tuple_1)

# Non-membership
print('Hello' not in string_1)
print('Hi' not in string_1)
print('a' not in tuple_1)
print('d' not in tuple_1)